# -------------------- CSV（客户统计+明细，产品/领导最爱，规范排版） --------------------
def export_csv(violations, page_name,stats, path):
    with open(path, "w", encoding="utf-8") as f:
        # 先写报告基础信息
        f.write("报告基础信息,内容\n")
        f.write(f"检测页面名称,{page_name}\n")
        # f.write(f"检测页面URL,{page_url}\n")
        f.write(f"检测时间,{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write(f"检测依据,WCAG 2.1 AA标准、GB/T 38604-2020国家标准、axe-core官方规则集\n")
        f.write("\n")
        # 核心统计（客户第一眼看到）
        f.write("统计项,数值,说明\n")
        f.write(f"总缺陷数,{stats['total']},包含所有等级缺陷\n")
        f.write(f"严重缺陷,{stats['critical']},必须整改，影响无障碍使用\n")
        f.write(f"较严重缺陷,{stats['serious']},必须整改，影响核心功能使用\n")
        f.write(f"一般缺陷,{stats['moderate']},建议整改，提升使用体验\n")
        f.write(f"轻微缺陷,{stats['minor']},优化即可，不影响核心使用\n")
        f.write(f"合规率,{stats['compliance_rate']}%,基于严重/较严重缺陷计算\n")
        f.write("\n")
        # 缺陷明细（完整、规范，客户可直接用于整改）
        f.write("序号,缺陷ID,缺陷等级,问题描述,修复建议,违反标准,检测页面\n")
        for idx, v in enumerate(violations, 1):
            desc = v["description"].replace(",", " ").replace("\n", " ")
            fix = v["help"].replace(",", " ").replace("\n", " ")
            standard = "WCAG 2.1 AA 标准、GB/T 38604-2020 国家标准"
            f.write(f"{idx},{v['id']},{v['impact'].upper()},{desc},{fix},{standard},{page_name}\n")

