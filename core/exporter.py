def export_all(violations, page_name, page_url):
    os.makedirs("reports", exist_ok=True)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    base_path = f"reports/{page_name}_{ts}"
    stats = calculate_stats(violations)

    export_csv(violations, page_name, stats, f"{base_path}.csv")
    export_json(violations, page_name, stats, f"{base_path}.json")
    export_html_detail(violations, page_name, page_url, stats, f"{base_path}_CN.html")
    export_pdf_detail_cn(violations, page_name, page_url, stats, f"{base_path}_CN.pdf")
