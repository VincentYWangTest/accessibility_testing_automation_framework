# -------------------- 中文PDF（商用交付版，排版精美，要素齐全） --------------------
def export_pdf_detail_cn(violations, page_name, page_url, stats, path):
    pdfmetrics.registerFont(UnicodeCIDFont("STSong-Light"))
    c = canvas.Canvas(path, pagesize=A4)
    width, height = A4

    # 标题（只使用支持的字体，不加粗）
    c.setFont("STSong-Light", 18)
    c.setFillColor(HexColor("#2c3e50"))
    c.drawCentredString(width/2, height-50, "Web 无障碍合规检测报告（商用版）")
    c.setFont("STSong-Light", 11)
    c.setFillColor(HexColor("#666666"))
    c.drawCentredString(width/2, height-70, f"检测页面：{page_name} | 检测时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    c.drawCentredString(width/2, height-85, f"页面URL：{page_url[:50]}..." if len(page_url) > 50 else f"页面URL：{page_url}")
    c.line(80, height-100, width-80, height-100)

    y = height - 120
    indent = 80

    # 1. 检测依据
    c.setFont("STSong-Light", 13)
    c.setFillColor(HexColor("#2c3e50"))
    c.drawString(indent, y, "一、检测依据")
    y -= 20
    c.setFont("STSong-Light", 10)
    c.setFillColor(HexColor("#333333"))
    for standard in DETECTION_STANDARDS["cn"]:
        if len(standard) > 70:
            lines = [standard[i:i+70] for i in range(0, len(standard), 70)]
            for line in lines:
                c.drawString(indent+10, y, line)
                y -= 15
        else:
            c.drawString(indent+10, y, standard)
            y -= 15
    y -= 10

    # 2. 统计
    c.setFont("STSong-Light", 13)
    c.setFillColor(HexColor("#2c3e50"))
    c.drawString(indent, y, "二、检测统计概览")
    y -= 20
    c.setFont("STSong-Light", 11)
    stats_items = [
        ("总缺陷数", stats['total'], HexColor("#2c3e50")),
        ("严重缺陷（必须整改）", stats['critical'], HexColor("#dc3545")),
        ("较严重缺陷（必须整改）", stats['serious'], HexColor("#fd7e14")),
        ("一般缺陷（建议整改）", stats['moderate'], HexColor("#ffc107")),
        ("轻微缺陷（优化即可）", stats['minor'], HexColor("#6c757d")),
        ("无障碍合规率", f"{stats['compliance_rate']}%", HexColor("#28a745"))
    ]
    for name, value, color in stats_items:
        c.setFillColor(color)
        c.drawString(indent+10, y, f"{name}：{value}")
        y -= 18
    y -= 10
    c.setFillColor(HexColor("#333333"))
    c.line(indent, y, width-indent, y)
    y -= 20

    # 3. 缺陷明细
    c.setFont("STSong-Light", 13)
    c.setFillColor(HexColor("#2c3e50"))
    c.drawString(indent, y, "三、缺陷详细信息")
    y -= 20
    c.setFont("STSong-Light", 10)

    if not violations:
        c.drawString(indent+10, y, "未检测到任何无障碍缺陷，网站符合WCAG 2.1 AA及GB/T 38604-2020标准要求。")
        y -= 20
    else:
        for idx, v in enumerate(violations, 1):
            if y < 80:
                c.showPage()
                c.setFont("STSong-Light", 16)
                c.setFillColor(HexColor("#2c3e50"))
                c.drawCentredString(width/2, height-50, f"缺陷明细（第{idx//20 + 1}页）")
                c.setFont("STSong-Light", 10)
                c.setFillColor(HexColor("#333333"))
                y = height - 80
                indent = 80

            if v["impact"] == "critical":
                c.setFillColor(HexColor("#dc3545"))
            elif v["impact"] == "serious":
                c.setFillColor(HexColor("#fd7e14"))
            elif v["impact"] == "moderate":
                c.setFillColor(HexColor("#ffc107"))
            else:
                c.setFillColor(HexColor("#6c757d"))
            
            c.drawString(indent+10, y, f"{idx}. 缺陷ID：{v['id']}【{v['impact'].upper()}】")
            y -= 15
            c.setFillColor(HexColor("#333333"))

            desc = v["description"]
            if len(desc) > 70:
                desc_lines = [desc[i:i+70] for i in range(0, len(desc), 70)]
                for line in desc_lines:
                    c.drawString(indent+20, y, f"描述：{line}")
                    y -= 15
            else:
                c.drawString(indent+20, y, f"描述：{desc}")
                y -= 15

            fix = v["help"]
            if len(fix) > 70:
                fix_lines = [fix[i:i+70] for i in range(0, len(fix), 70)]
                for line in fix_lines:
                    c.drawString(indent+20, y, f"建议：{line}")
                    y -= 15
            else:
                c.drawString(indent+20, y, f"建议：{fix}")
                y -= 15

            c.drawString(indent+20, y, "违反标准：WCAG 2.1 AA 标准、GB/T 38604-2020 国家标准")
            y -= 20
            c.line(indent+10, y+5, width-indent-10, y+5)
            y -= 15

    # 4. 免责声明
    if y < 120:
        c.showPage()
        c.setFont("STSong-Light", 16)
        c.setFillColor(HexColor("#2c3e50"))
        c.drawCentredString(width/2, height-50, "免责声明")
        c.setFont("STSong-Light", 10)
        c.setFillColor(HexColor("#333333"))
        y = height - 80
        indent = 80
    else:
        c.setFont("STSong-Light", 13)
        c.setFillColor(HexColor("#2c3e50"))
        c.drawString(indent, y, "四、免责声明")
        y -= 20
        c.setFont("STSong-Light", 10)
        c.setFillColor(HexColor("#333333"))

    disclaimer_lines = DISCLAIMER["cn"].split("\n")
    for line in disclaimer_lines:
        if line.strip():
            if len(line) > 75:
                sub_lines = [line[i:i+75] for i in range(0, len(line), 75)]
                for sub_line in sub_lines:
                    c.drawString(indent+10, y, sub_line)
                    y -= 15
            else:
                c.drawString(indent+10, y, line)
                y -= 15

    # 页脚
    y -= 20
    c.setFont("STSong-Light", 9)
    c.setFillColor(HexColor("#666666"))
    c.drawCentredString(width/2, y, "检测人：XXX | 联系方式：XXX | 报告仅供商用参考，具备交付效力")
    c.drawCentredString(width/2, y-12, f"报告生成时间：{datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")

    c.save()
    logger.info(f"✅ 中文商用PDF已生成：{path}")

